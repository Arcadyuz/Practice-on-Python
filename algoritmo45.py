#Mostrar la cantidad de numeros pares desde 1 hasta 100
numero=1
cantidad=0
while numero <= 100:
    #print("Los numeros pares son: ",numero)
    numero= numero+2
    cantidad=cantidad+1
print("La cantidad de numero pares son: ",cantidad)